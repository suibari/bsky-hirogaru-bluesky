<h5>エンゲージメント</h5>
  <ul>
    <li>エンゲージメントとはあるユーザから他のユーザへの関心度を表す数値で、相関図の配置に影響します</li>
    <li>あるユーザの直近10000件のポストと2000件のいいねを取得します</li>
    <li>ただし初回実行のユーザの場合、取得時間短縮のため直近のポスト200件といいね20件のみ取得します。これを制限モードと呼びます</li>
    <li>誰に対するリプライまたはいいねか識別し、リプライは10点、いいねは1点でユーザごとに加点します</li>
    <li>これであるユーザから各ユーザに対するエンゲージメントを得ます</li>
  </ul>
<h5>相関図</h5>
  <ul>
    <li>エンゲージメントを上位順にソートし、内側から配置します</li>
    <li>通常は331名(半径10アイコン分)を配置し、制限モードでは37名を配置します</li>
    <li>同じ得点だった場合は、より新しいリプライをした方が優先されます</li>
  </ul>
<h5>影響度</h5>
  <ul>
    <li>そのユーザの影響力を示す数値で、相関図のアイコンの大きさに影響します</li>
    <li>log<sub>10</sub>(1000×フォロワーの数/フォローの数)×30-50</li>
    <li>これを最低値20、最高値60でクリップします</li>
    <li>対数変換は分散を減らすために行っています</li>
  </ul>
<h5>データ更新</h5>
  <ul>
    <li>Generateボタンを押した際、データベースに格納されたデータがあればそれを表示し、なければ制限モードでデータ取得と表示を行います</li>
    <li>ユーザへの相関図表示とともに、サーバ側でデータ収集とデータベースへの格納を行います</li>
    <li>ポスト数、いいね数にもよりますが約1~3分で処理は完了するはずです</li>
    <li>処理完了で通知を発行することはありません</li>
    <li>つまり、「現在見ている相関図は、前回実行時に取得されたデータによる相関図」ということです</li>
  </ul>
<h5>ユーザカード</h5>
  <ul>
    <li>アイコンをクリックまたはタップするとユーザカードが出ます</li>
    <li>ユーザカードには相関図中心の人からその人へのリプライ数といいね数が出ます</li>
    <li>これは全てのポスト、いいねを集計しているわけではなく、エンゲージメントの項目に記載したポスト数、いいね数に準じます</li>
    <li>ハイフンになっている場合、次回データ更新時に反映されます。時間をおいて再度実行してみてください</li>
  </ul>