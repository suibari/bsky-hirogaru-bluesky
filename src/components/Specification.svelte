</p>
<h5>エンゲージメント</h5>
  <ul>
    <li>エンゲージメントとはあるユーザから他のユーザへの関心度を表す数値で、相関図の配置に影響します</li>
    <li>あるユーザの直近1000件のポストと100件のいいねを取得します</li>
    <li>誰に対するリプライまたはいいねか識別し、リプライは10点、いいねは1点でユーザごとに加点します</li>
    <li>これであるユーザから各ユーザに対するエンゲージメントを得ます</li>
  </ul>
<h5>相関図</h5>
  <ul>
    <li>エンゲージメントを上位順にソートし、内側から上位36名を掲載します</li>
    <li>同じ得点だった場合は、より新しいリプライをした方が優先されます</li>
  </ul>
<h5>影響度</h5>
  <ul>
    <li>そのユーザの影響力を示す数値で、相関図のアイコンの大きさに影響します</li>
    <li>log<sub>10</sub>(1000×フォロワーの数/フォローの数)×30-50</li>
    <li>これを最低値20、最高値60でクリップします</li>
    <li>対数変換は分散を減らすために行っています</li>
  </ul>
<h5>片思い率</h5>
  <ul>
    <li>相手から自分のエンゲージメントを上記と同様に求めます</li>
    <li>100×小さい方のエンゲージメント/大きい方のエンゲージメント が片思い率です</li>
    <li>片思い率が50%未満なら「片思い」と出ます</li>
    <li>50%以上なら「相思相愛」と出ます</li>
  </ul>
<h5>関係性バイアス</h5>
  <ul>
    <li>自分と相手の大きい方のエンゲージメントを10で割った数です</li>
    <li>最大値50(つまりエンゲージメント500)でクリップします</li>
    <li>関係性バイアス20以下なら「みがけば光る」と出ます</li>
    <li>20〜50未満なら「最近話題の」と出ます</li>
    <li>50、つまりMAXなら「Bluesky中にとどろく」と出ます</li>
  </ul>